import Foundation

class Teachers {
    var tName : String
    var tSurname : String
    var tAge : Int
    var lesson : String
    var workExperience : Int
    
    init(tName:String, tSurname:String, tAge:Int, lesson:String, workExperience:Int){
        self.tName = tName
        self.tSurname = tSurname
        self.tAge = tAge
        self.lesson = lesson
        self.workExperience = workExperience
    }
  
}
